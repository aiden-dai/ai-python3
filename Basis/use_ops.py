print(9 // 5)  # 1
print(9 / 5)  # 1.8
print(9 % 5)  # 4 
print(3 ** 2)  # 9
print(16 >> 2)  # 16 / 2^2 = 4
print(16 << 2)  # 16 * 2^2 = 64

print(3 | 2)  # 3 = 11, 2 = 10,  3 | 2 = 11 | 10 = 11 -> 3
print(3 & 2)  # 3 = 11, 2 = 10,  3 & 2 = 11 & 10 = 10 -> 2
print(3 ^ 2)  # 3 = 11, 2 = 10,  3 ^ 2 = 11 & 10 = 01 -> 1